	public Integer search(String code) {
		
		// search for barcode
		for (DbEntry entry : this.database) {
			for (String wildcard : entry.codes) {
				if (code.matches(wildcard)) {
					return entry.identifier;
				}
			}
		}
		
		return 0;
	}
